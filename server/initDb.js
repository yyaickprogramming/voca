async function createTables() {
  await db.exec(`
    CREATE TABLE IF NOT EXISTS users (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      username TEXT NOT NULL,
      password TEXT NOT NULL
    );

    CREATE TABLE IF NOT EXISTS ideas (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      title TEXT NOT NULL,
      description TEXT NOT NULL,
      pitch TEXT NOT NULL DEFAULT '',
      why_now TEXT NOT NULL DEFAULT '',
      first_steps TEXT NOT NULL DEFAULT '',
      audience TEXT NOT NULL,
      risks TEXT NOT NULL,
      income TEXT NOT NULL,
      cost TEXT NOT NULL,
      difficulty TEXT NOT NULL,
      relevance TEXT NOT NULL
    );

    CREATE TABLE IF NOT EXISTS user_ideas (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      idea_id INTEGER NOT NULL,
      is_main INTEGER NOT NULL DEFAULT 0,
      FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
      FOREIGN KEY (idea_id) REFERENCES ideas(id) ON DELETE CASCADE
    );

    CREATE TABLE IF NOT EXISTS tasks (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      text TEXT NOT NULL,
      completed INTEGER NOT NULL DEFAULT 0,
      FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
    );
  `);

  console.log('Таблицы SQLite созданы или уже существуют.');
}

async function ensureIdeasConstraints() {
  await db.run('CREATE UNIQUE INDEX IF NOT EXISTS idx_ideas_title ON ideas(title)');
}

async function ensureUserIdeasConstraints() {
  await db.run(`
    DELETE FROM user_ideas
    WHERE id NOT IN (
      SELECT MIN(id)
      FROM user_ideas
      GROUP BY user_id, idea_id
    )
  `);

  await db.run(
    'CREATE UNIQUE INDEX IF NOT EXISTS idx_user_ideas_unique ON user_ideas(user_id, idea_id)'
  );
}

async function ensureUsersConstraints() {
  await db.run(`
    DELETE FROM users
    WHERE id NOT IN (
      SELECT MIN(id)
      FROM users
      GROUP BY lower(trim(username))
    )
  `);

  await db.run(
    'CREATE UNIQUE INDEX IF NOT EXISTS idx_users_username_normalized ON users(lower(trim(username)))'
  );
}

async function seedIdeas() {
  console.log('Обновляем идеи в базе...');

  for (const idea of allIdeas) {
    await db.run(
      `
        INSERT INTO ideas
          (title, description, pitch, why_now, first_steps, audience, risks, income, cost, difficulty, relevance)
        VALUES
          (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        ON CONFLICT(title) DO UPDATE SET
          description = excluded.description,
          pitch = excluded.pitch,
          why_now = excluded.why_now,
          first_steps = excluded.first_steps,
          audience = excluded.audience,
          risks = excluded.risks,
          income = excluded.income,
          cost = excluded.cost,
          difficulty = excluded.difficulty,
          relevance = excluded.relevance
      `,
      [
        idea.title,
        idea.description,
        idea.pitch,
        idea.why_now,
        idea.firstSteps,
        idea.audience,
        idea.risks,
        idea.income,
        idea.cost,
        idea.difficulty,
        idea.relevance
      ]
    );
  }

  console.log('Идеи готовы к использованию.');
}

async function initializeDatabase() {
  await createTables();
  await ensureIdeasConstraints();
  await ensureUserIdeasConstraints();
  console.log('Ограничения для сохранённых идей готовы.');
  await ensureUsersConstraints();
  console.log('Ограничения для логинов готовы.');
  await seedIdeas();
}

